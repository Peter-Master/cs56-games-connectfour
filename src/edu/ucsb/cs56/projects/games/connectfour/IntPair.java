package edu.ucsb.cs56.projects.games.connectfour;

public class IntPair {
    
    private int x;
    private int y;

    public IntPair(int x, int y) {
	this.x = x;
	this.y = y;
    }

    public int getX() {
	return this.x;
    }
    
    public int getY() {
	return this.y;
    }

    public void setX(int x) {
	this.x = x;
    }
    
    public void setY(int y) {
	this.y = y;
    }
}
